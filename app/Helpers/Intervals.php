<?php

namespace App\Helpers;

class Intervals
{
    public static function titles()
    {
        return ['1m','3m','5m','15m','30m','1h','2h','4h','6h','8h','12h','1d'];
    }

    public static function ms()
    {
        return [
            '1m' => 1*60*1000,
            '3m' => 3*60*1000,
            '5m' => 5*60*1000,
            '15m' => 15*60*1000,
            '30m' => 30*60*1000,
            '1h' => 1*60*60*1000,
            '2h' => 2*60*60*1000,
            '4h' => 4*60*60*1000,
            '6h' => 6*60*60*1000,
            '8h' => 8*60*60*1000,
            '12h' => 12*60*60*1000,
            '1d' => 24*60*60*1000
        ];
    }

    public static function days()
    {
        return [
            '1m' => 1/(24*60),
            '3m' => 1/(24*60)*3,
            '5m' => 1/(24*60)*5,
            '15m' => 1/(24*60)*15,
            '30m' => 1/(24*60)*30,
            '1h' => 1/24,
            '2h' => 1/24*2,
            '4h' => 1/24*4,
            '6h' => 1/24*6,
            '8h' => 1/24*8,
            '12h' => 1/24*12,
            '1d' => 1,
            '3d' => 3,
            '1w' => 7,
            '1M' => 30
        ];
    }
}
